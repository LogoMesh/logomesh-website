#!/usr/bin/env node
/**
 * Regenerates lib/harness-public.generated.ts from logomesh-startup/results/real_prs_run_*.json
 *
 * Usage:
 *   npm run sync:harness
 *   LOGOMESH_RESULTS_DIR=/path/to/results npm run sync:harness
 */

import fs from "fs";
import path from "path";

const cwd = process.cwd();
const resultsDir =
  process.env.LOGOMESH_RESULTS_DIR ||
  path.join(cwd, "..", "logomesh-startup", "results");
const outFile = path.join(cwd, "lib", "harness-public.generated.ts");

function listRunFiles(dir) {
  if (!fs.existsSync(dir)) {
    console.error(`Results directory not found: ${dir}`);
    process.exit(1);
  }
  return fs
    .readdirSync(dir)
    .filter((n) => /^real_prs_run_\d+\.json$/.test(n))
    .sort(
      (a, b) =>
        Number(a.replace(/\D/g, "")) - Number(b.replace(/\D/g, "")),
    )
    .map((n) => path.join(dir, n));
}

function escStr(s) {
  return JSON.stringify(s ?? "");
}

const runFiles = listRunFiles(resultsDir);

let totalRows = 0;
let confirmedSum = 0;
let commentRows = 0;
const pairs = new Set();
const repos = new Set();
const candidates = [];

for (const file of runFiles) {
  const raw = fs.readFileSync(file, "utf8");
  const data = JSON.parse(raw);
  if (!Array.isArray(data)) continue;
  for (const row of data) {
    totalRows += 1;
    const repo = row.repo || "";
    const prn = row.pr_number;
    if (repo && prn != null) {
      pairs.add(`${repo}#${prn}`);
      repos.add(repo);
    }
    const cf = Number(row.confirmed_findings || 0);
    confirmedSum += cf;
    if (row.has_comment) commentRows += 1;
    const st = row.status || "";
    if (cf > 0 && st === "ok") {
      const fn = row.filename || "";
      const basename = fn.includes("/") ? fn.split("/").pop() : fn;
      candidates.push({
        repo,
        pr_number: Number(prn) || 0,
        title: String(row.pr_title || "").slice(0, 200),
        file_basename: String(basename || "").slice(0, 120),
        confirmed_findings: cf,
      });
    }
  }
}

candidates.sort(
  (a, b) =>
    b.confirmed_findings - a.confirmed_findings || a.repo.localeCompare(b.repo),
);

const seenRepo = new Set();
const picked = [];
for (const c of candidates) {
  if (seenRepo.has(c.repo)) continue;
  seenRepo.add(c.repo);
  picked.push(c);
  if (picked.length >= 8) break;
}

const lines = [];
lines.push("// AUTO-GENERATED by scripts/sync-harness-from-results.mjs — do not edit by hand.");
lines.push("// Source: logomesh-startup/results/real_prs_run_*.json");
lines.push("");
lines.push("export const HARNESS_AGGREGATE = {");
lines.push(`  batchRunFiles: ${runFiles.length},`);
lines.push(`  fileRowsTotal: ${totalRows},`);
lines.push(`  uniquePullRequests: ${pairs.size},`);
lines.push(`  distinctRepos: ${repos.size},`);
lines.push(`  confirmedFindingsSum: ${confirmedSum},`);
lines.push(`  fileRowsWithPrComment: ${commentRows},`);
lines.push('  source: "logomesh-startup/results/real_prs_run_*.json",');
lines.push("} as const;");
lines.push("");
lines.push("export type HarnessShowcaseRow = {");
lines.push("  repo: string;");
lines.push("  prNumber: number;");
lines.push("  title: string;");
lines.push("  fileBasename: string;");
lines.push("  confirmedFindings: number;");
lines.push("};");
lines.push("");
lines.push(
  "/** Diverse OSS PRs where the harness surfaced at least one confirmed finding (one row per repo). */",
);
lines.push("export const HARNESS_SHOWCASE: readonly HarnessShowcaseRow[] = [");

for (const p of picked) {
  lines.push("  {");
  lines.push(`    repo: ${escStr(p.repo)},`);
  lines.push(`    prNumber: ${p.pr_number},`);
  lines.push(`    title: ${escStr(p.title)},`);
  lines.push(`    fileBasename: ${escStr(p.file_basename)},`);
  lines.push(`    confirmedFindings: ${p.confirmed_findings},`);
  lines.push("  },");
}
lines.push("];");
lines.push("");

fs.writeFileSync(outFile, lines.join("\n"), "utf8");
console.log(`Wrote ${outFile}`);
console.log(
  `From ${runFiles.length} batch files → ${pairs.size} PRs, ${confirmedSum} confirmed findings (sum), ${picked.length} showcase rows`,
);
